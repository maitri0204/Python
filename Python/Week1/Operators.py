# Arithmetic operators
print("Addition: ", 2+3)
print("Subtraction: ",9-5)
print("Multiplication: ",5*4)
print("Division: ", 7/3)
print("Floor Division: ", 7//3)
print("Modulus: ",7%3)
print("Exponential: ",6**2)

# Relational operators
print(5 > 10)
print(5 < 10)
print(5 >= 5)
print(5 < 5)
print(5 == 10)
print(5 != 12)

# Logical operators
print("Logical AND Operator")
print(True and True)
print(True and False)
print(False and True)
print(False and False)

print("Logical OR Operator")
print(True or True)
print(True or False)
print(False or True)
print(False or False)

print("Logical NOT Operator")
print(not True)
print(not False)